public class Point {
	private double x;
	private double y;
	
 public Point() {}

 public Point(double newX, double newY) {
	 x = newX;
	 y = newY;
 }

 public double getX() {
	 return x;
 }

 public double getY() {
	 return y;
 }

 public void setX(int newX) {
	 x = newX;
 }

 public void setY(int newY) {
	 y = newY;
 } 
} 